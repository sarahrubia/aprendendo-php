<?php
// Definindo variáveis

# São definidas com $ e o nome da variável
$nome_da_variavel;

# Definir e iniciar uma variável
$nome_da_variavel = 100;
echo $nome_da_variavel;

# Podemos alterar o tipo de valores de uma variável, ex: int para string
$nome_da_variavel = "teste";
echo $nome_da_variavel;

# Não pode iniciar nome de variáveis com números, símbolos (além de $ e _), não pode conter espaços e hífens

# Convenções PHP Standard Conventions
$umaVariavel = 0; # camel case
$uma_variavel = 1; # snake case
$UmaVariavel = 2; # studly case (não recomendado)
